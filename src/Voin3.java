public class Voin3 {
    String s;
    String t;
    String v;

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    @Override
    public String toString() {
        return
                s + "\n"+
                t + "\n"+
                v + "\n"
                ;
    }
}
